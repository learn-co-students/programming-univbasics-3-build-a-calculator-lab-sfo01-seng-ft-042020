def addition(num1, num2)
  # add num2 to num1
  puts "num1 + num2"
  # return the result of the calculation
  num1 + num2
end

def subtraction(num1, num2)
  # subtract num2 from num1
  puts "num1 - num2"
  # return the result of the calculation
  num1 - num2
end

def division(num1, num2)
  # divide num2 into num1
  puts "num1 / num2"
  # return the result of the calculation
  num1 / num2
end

def multiplication(num1, num2)
  # multiplie num1 by num2
  puts "num1 * num2"
  # return the result of the calculation
  num1 * num2
end

def modulo(num1, num2)
  # divide num2 into num1
  puts "num1 % num2"
  # return the remainder of the calculation
  num1 % num2
end

def square_root(num)
  # find the square root of num
  puts "Math.sqrt(num)"
  # returns the square root
  Math.sqrt(num)
end